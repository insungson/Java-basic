package homework_day4;

public class Book {

	int bookNo=0;
	String title="";
	String author="";
	int stateCode=0;	//생성할때 1로 생성
	
	
	
	public Book(int bookNo, String title, String author) {
		//super();
		this.bookNo = bookNo;
		this.title = title;
		this.author = author;
		this.stateCode = (stateCode = 1);
	}
	public int getBookNo() {
		return bookNo;
	}
	public void setBookNo(int bookNo) {
		this.bookNo = bookNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public void rent () {
		stateCode = 0;
		System.out.println(title + "이(가) 대여 됐습니다.");
	}
	public void print() {
		System.out.println("책 제목:"+title+", 작가:"+author+", 대여 유무:"+(stateCode == 1 ? "재고있음":"재고없음"));	
	}															//stateCode가 1이면 재고있음나오고 1이 아니면 재고없음이 나옴...
	
	

	
}
